const grade = 'A'

switch (grade) {
    case 'A':
        console.log('your got an A!')
        break
    case 'B':
        console.log('your got a B!')
        break
    case 'C':
        console.log('your got a C!')
        break
    case 'D':
        console.log('your got a D!')
        break
    case 'E':
        console.log('your got a E!')
        break
    default:
        console.log('not a valid grade.')
        break
}